namespace Bosel.Model.Common.Source
{
    public class Paths
    {
        /// <summary>
        /// Gets or sets the directory where are the source files.
        /// </summary>
        /// <value>
        /// The path in.
        /// </value>
        public string PathIn { get; set; }
        /// <summary>
        /// Gets or sets the directory where the report will be saved.
        /// </summary>
        /// <value>
        /// The path out.
        /// </value>
        public string PathOut { get; set; }

        /// <summary>
        /// Gets or sets the path where are the settings.
        /// </summary>
        /// <value>
        /// The path settings.
        /// </value>
        public string PathSettings { get; set; }
    }
}
